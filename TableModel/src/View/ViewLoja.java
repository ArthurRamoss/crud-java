    private void JButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JButtonSalvarActionPerformed
        Loja L = new Loja();
        L.setCEP(Integer.parseInt(txtCEP.getText()));    
        L.setTel(Integer.parseInt(txtTel.getText()));   
        L.setNum(Integer.parseInt(txtNum.getText()));   
        L.setID(Integer.parseInt(txtID.getText()));   
        L.setNome(txtNome.getText());
        L.setEstado(txtEstado.getText());
        L.setCidade(txtCidade.getText());
        L.setBairro(txtBairro.getText());
        
        tableModel.addRow(L);
    }//GEN-LAST:event_JButtonSalvarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
       if(JTLoja.getSelectedRow()!= -1){
           tableModel.removeRow(JTLoja.getSelectedRow());
       }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        // TODO add your handling code here:
        if(JTLoja.getSelectedRow() != -1){
            tableModel.setValueAt(txtID.getText(), JTLoja.getSelectedRow(), 0);
            tableModel.setValueAt(txtNome.getText(), JTLoja.getSelectedRow(), 1);
            tableModel.setValueAt(txtTel.getText(), JTLoja.getSelectedRow(), 2);
            tableModel.setValueAt(txtCEP.getText(), JTLoja.getSelectedRow(), 3);
            tableModel.setValueAt(txtEstado.getText(), JTLoja.getSelectedRow(), 4);
            tableModel.setValueAt(txtCidade.getText(), JTLoja.getSelectedRow(), 5);
            tableModel.setValueAt(txtBairro.getText(), JTLoja.getSelectedRow(), 6);
            tableModel.setValueAt(txtNum.getText(), JTLoja.getSelectedRow(), 7);
        }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

